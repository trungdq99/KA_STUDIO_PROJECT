function showButton3Info()
{
	document.getElementById("button3Info").style.display="block";
	document.getElementById("closeButton3Info").style.display="block";
	document.getElementById("showButton3Info").style.display="none";
}

function closeButton3Info()
{
	document.getElementById("button3Info").style.display="none";
	document.getElementById("closeButton3Info").style.display="none";
	document.getElementById("showButton3Info").style.display="block";
}